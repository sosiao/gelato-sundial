package com.yizlan.gelato.sundial.filling;

import com.yizlan.gelato.sundial.complex.BinaryBaseExtractor;
import com.yizlan.gelato.sundial.complex.UpdateExtractor;

import java.util.Date;

/**
 * demo teacher
 *
 * @author Zen Gershon
 * @since 1.3.0
 */
public class Teacher implements BinaryBaseExtractor<String, Date>,UpdateExtractor<String, Date> {

    private String id;

    private String name;

    private String createBy;

    private Date createTime;

    private String updateBy;
    
    private Date updateTime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String getCreateBy() {
        return createBy;
    }

    @Override
    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }

    @Override
    public Date getCreateTime() {
        return createTime;
    }

    @Override
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    @Override
    public String getUpdateBy() {
        return updateBy;
    }

    @Override
    public void setUpdateBy(String updateBy) {
        this.updateBy = updateBy;
    }

    @Override
    public Date getUpdateTime() {
        return updateTime;
    }

    @Override
    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    @Override
    public String toString() {
        return "Teach{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", createBy='" + createBy + '\'' +
                ", createTime=" + createTime +
                ", updateBy='" + updateBy + '\'' +
                ", updateTime=" + updateTime +
                '}';
    }
}
